void solve()
{
    int n, m;
    cin >> n >> m;
    ll a[n][m];
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> a[i][j];
        }
    }
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            ll maxi = 0;
            if (i > 0)
            {
                maxi = max(maxi, a[i - 1][j]);
            }
            if (i + 1 < n)
                maxi = max(maxi, a[i + 1][j]);
            if (j > 0)
                maxi = max(maxi, a[i][j - 1]);
            if (j + 1 < m)
                maxi = max(maxi, a[i][j + 1]);

            if (a[i][j] > maxi)
                a[i][j] = maxi;
        }
    }
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cout << a[i][j] << " ";
        }
        cout << '\n';
    }
}
