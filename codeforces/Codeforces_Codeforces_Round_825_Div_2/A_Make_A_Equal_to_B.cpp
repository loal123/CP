void solve()
{
    int ans1 = 0, ans2 = 0;
    int n;
    cin >> n;
    int cnt1 = 0, cnt2 = 0;
    vi a(n), b(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++)
    {
        cin >> b[i];
    }
    for (int i = 0; i < n; i++)
    {
        ans1 += (a[i] ^ b[i]);
        cnt1 += (a[i] & 1);
        cnt2 += (b[i] & 1);
    }
    cout << min(ans1, abs(cnt1 - cnt2) + 1) << '\n';
}
