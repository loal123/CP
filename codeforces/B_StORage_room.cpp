void solve()
{
    int n;
    cin >> n;
    int m[n][n];
    ll ans = 1;
    ans <<= 30;
    ans--;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        a[i] = ans;
    }
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            cin >> m[i][j];
        }
    }
    bool can = true;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (i != j)
            {
                a[i] &= m[i][j];
                a[j] &= m[i][j];
            }
        }
    }
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (i != j)
            {
                if ((a[i] | a[j]) != m[i][j])
                    can = false;
            }
        }
    }
    if (!can)
        cout << "NO\n";
    else
    {
        cout << "YES\n";
        for (int i = 0; i < n; i++)
        {
            cout << a[i] << " ";
        }

        cout << '\n';
    }
}
